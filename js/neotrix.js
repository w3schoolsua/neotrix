/**
 * Швидкий селектор одного елемента
 * @param {string} selector
 * @returns {Element|null}
 */
function ntx$(selector) {
    return document.querySelector(selector);
}

/**
 * Швидкий селектор множини елементів
 * @param {string} selector
 * @returns {NodeList}
 */
function ntx$all(selector) {
    return document.querySelectorAll(selector);
}


